public class InappropriateLineLengthException extends RuntimeException {
    public InappropriateLineLengthException() {
        super();
    }

    public InappropriateLineLengthException(String message) {
        super(message);
    }

    public InappropriateLineLengthException(String message, Throwable cause) {
        super(message, cause);
    }

    public InappropriateLineLengthException(Throwable cause) {
        super(cause);
    }
}
